//=============================================================================
// シーン処理の実行
//=============================================================================
bool SceneInit::Run()
{
	if (m_bStart) {
		Start();
		m_bStart = false;
	}
	
	Update();

	if (m_nowSceneData.m_scene != m_prevSceneData.m_scene) {
		m_bEnd = true;
	}

	return m_bEnd;
}
