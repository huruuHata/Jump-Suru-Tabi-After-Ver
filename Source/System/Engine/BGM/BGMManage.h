#pragma once

//DirectShowを使うと色々な警告が出るため、警告が出ないよう設定しておく
#pragma warning(disable : 4995)

#include <dshow.h>

#include <string>

#include "..\\Exception\\DxSystemException.h"

class BGMManage
{
public:

	//=============================================================================
	// メンバの初期化（コンストラクタ）
	//=============================================================================
	BGMManage();

	//=============================================================================
	// デストラクタ
	//=============================================================================
	~BGMManage();

	//=============================================================================
	// ＢＧＭ管理の初期化
	//=============================================================================
	void Initializie();

	//=============================================================================
	// DirectShow関連の初期化およびメディアファイルの読み込み
	// 引　数：string メディアファイル名（mp3）
	//=============================================================================
	void AddBGM(std::string strFileName);

	//=============================================================================
	// DirectShow関連領域の解放
	//=============================================================================
	void ReleaseBGM();

	//=============================================================================
	// メディアの再生
	//=============================================================================
	void PlayBGM();

	//=============================================================================
	// メディアの停止
	//=============================================================================
	void StopBGM();

	//=============================================================================
	// ループ再生
	//=============================================================================
	void LoopBGM();

	//=============================================================================
	// ボリュームをリセット
	//=============================================================================
	void ResetBGMVolume();

	//=============================================================================
	// ボリュームを調整
	// 引　数：const int 増減させる数値
	//=============================================================================
	void ChangeBGMVolume(const int value);

	//=============================================================================
	// パンをリセット
	//=============================================================================
	void ResetBGMPan();

	//=============================================================================
	// パンを調整
	// 引　数：const int 増減させる数値
	//=============================================================================
	void ChangeBGMPan(const int value);

	//=============================================================================
	// 再生中チェック
	// 戻り値：bool 再生中 true  それ以外 false
	//=============================================================================
	bool IsPlaying();

	//=============================================================================
	// 再生中チェック
	// 戻り値：bool 再生中 true  それ以外 false
	// 引　数：string メディアファイル名（mp3）
	//=============================================================================
	bool IsPlaying(std::string strFileName);

private:

	//グラフビルダー
	IGraphBuilder* m_pGraphBuilder;

	//メディアコントロール用
	IMediaControl* m_pMediaControl;

	//イベント取得用
	IMediaEvent*   m_pMediaEvent;

	//巻き戻し用
	IMediaSeeking* m_pMediaSeeking;
	
	//ボリューム、パン調整用
	IBasicAudio* m_pBasicAudio;

	//現在のボリューム
	int m_volume;

	//現在のパン
	int m_pan;

	//再生中フラグ
	bool m_bPlaying;

	//ＢＧＭファイル名
	std::string m_loadedBGMFilename;

	//-----------------------------------------------------------------------------
	// 終了チェック
	// 戻り値：bool true 終了、false 再生中
	//-----------------------------------------------------------------------------
	bool EndCheck();

	//-----------------------------------------------------------------------------
	// メディアの巻き戻し
	//-----------------------------------------------------------------------------
	void Rewind();
};